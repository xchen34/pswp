#include "push_swap.h"

int	check_all_space(char *s)
{
	while (*s)
	{
		if (*s == '\t' || *s == '\n' || *s == '\v'
			|| *s == '\f' || *s == '\r' || *s == ' ')
			s++;
		else
			return (0);
	}
	return (1);
}
